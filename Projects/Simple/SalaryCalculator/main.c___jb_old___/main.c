#include <stdio.h>

int main() {

    // Hard coding the values to a Array
    int numbers[10];

    for (int i = 0; i < 10; i++){

        numbers[i] = i + 1;

        printf("%d\t", numbers[i]);

    }

    printf("\n");

    // Taking Input from user:
    int marks[10];

    for (int i = 0; i < 5; i++){

        printf("\n\nEnter the Marks of student: ");
        scanf("%d", &marks[i]);

        printf("Marks of student %d: %d", i+1, marks[i]);

    }


    return 0;
}